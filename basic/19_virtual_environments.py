# Desde terminal (no dentro de Python):
# Crear un entorno
# python -m venv venv_name

# Activar:
# Windows: venv\Scripts\activate
# Linux/Mac: source venv/bin/activate

# Instalar paquetes
# pip install package_name

# Listar paquetes instalados
# pip list

# Desactivar entorno
# deactivate
